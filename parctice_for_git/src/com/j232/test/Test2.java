package com.j232.test;

public class Test2 {
	public static void main(String[] args){
		// 1230000000000
		// 123 * 10^10
		System.out.println(1.23e12);
		System.out.println(1.23E12);
		//
		// 0 开头的数字代表八进制数
		System.out.println(0123);
		
		// 0x开头的是16进制数
		System.out.println(0x123);
		System.out.println(0X123);
		
	}
}
